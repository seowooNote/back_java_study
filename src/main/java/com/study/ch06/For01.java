package com.study.ch06;

public class For01 {

    public static void main(String[] args) {

        for(int i = 0; i < 10; i++) {
            System.out.println("i: " + i);
        }

        System.out.println("----------------------");

        int i = 0;
        while(i < 10) {
            System.out.println("i: " + i);
            i++;
        }

    }

}
