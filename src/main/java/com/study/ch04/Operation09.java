package com.study.ch04;

public class Operation09 {

    public static void main(String[] args) {
        /*
        * 증감연산자
        */

        int i = 0;
        // i++; // i += 1;
        System.out.println(++i); // 선증가연산
        System.out.println(++i);
        System.out.println(i++); // 후증가연산
        System.out.println(i++);

        System.out.println(--i); // 선감소연산
        System.out.println(i--); // 후감소연산

    }

}
